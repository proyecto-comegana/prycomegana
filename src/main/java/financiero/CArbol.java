package financiero;

public class CArbol {
	private long lCodigo;
	private int iNiveles;
	private int iElementos;
	private String sFecha;
	private String sHora;
	private long lCliente;
	private int iCupos;
	public long getlCodigo() {
		return lCodigo;
	}
	public void setlCodigo(long lCodigo) {
		this.lCodigo = lCodigo;
	}
	public int getiNiveles() {
		return iNiveles;
	}
	public void setiNiveles(int iNiveles) {
		this.iNiveles = iNiveles;
	}
	public int getiElementos() {
		return iElementos;
	}
	public void setiElementos(int iElementos) {
		this.iElementos = iElementos;
	}
	public String getsFecha() {
		return sFecha;
	}
	public void setsFecha(String sFecha) {
		this.sFecha = sFecha;
	}
	public String getsHora() {
		return sHora;
	}
	public void setsHora(String sHora) {
		this.sHora = sHora;
	}
	public long getlCliente() {
		return lCliente;
	}
	public void setlCliente(long lCliente) {
		this.lCliente = lCliente;
	}
	public int getiCupos() {
		return iCupos;
	}
	public void setiCupos(int iCupos) {
		this.iCupos = iCupos;
	}
}
