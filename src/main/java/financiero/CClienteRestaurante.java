package financiero;

public class CClienteRestaurante {
	private long lCliente;
	private long lRestaurante;
	private String sFecha;
	private String sHora;

	public long getlCliente() {
		return lCliente;
	}
	public void setlCliente(long lCliente) {
		this.lCliente = lCliente;
	}
	public long getlRestaurante() {
		return lRestaurante;
	}
	public void setlRestaurante(long lRestaurante) {
		this.lRestaurante = lRestaurante;
	}
	public String getsFecha() {
		return sFecha;
	}
	public void setsFecha(String sFecha) {
		this.sFecha = sFecha;
	}
	public String getsHora() {
		return sHora;
	}
	public void setsHora(String sHora) {
		this.sHora = sHora;
	}
	
}
