package financiero;

public class CNodoArbol {
	private long lNodo;
	private long lArbol;
	private String sFecha;
	private String sHora;
	public long getlNodo() {
		return lNodo;
	}
	public void setlNodo(long lNodo) {
		this.lNodo = lNodo;
	}
	public long getlArbol() {
		return lArbol;
	}
	public void setlArbol(long lArbol) {
		this.lArbol = lArbol;
	}
	public String getsFecha() {
		return sFecha;
	}
	public void setsFecha(String sFecha) {
		this.sFecha = sFecha;
	}
	public String getsHora() {
		return sHora;
	}
	public void setsHora(String sHora) {
		this.sHora = sHora;
	}
}
