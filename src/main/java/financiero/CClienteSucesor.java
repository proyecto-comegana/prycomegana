package financiero;

public class CClienteSucesor {
	private long lCliente;
	private long lSucesor;
	private String sFecha;
	private String sHora;
	public long getlCliente() {
		return lCliente;
	}
	public void setlCliente(long lCliente) {
		this.lCliente = lCliente;
	}
	public long getlSucesor() {
		return lSucesor;
	}
	public void setlSucesor(long lSucesor) {
		this.lSucesor = lSucesor;
	}
	public String getsFecha() {
		return sFecha;
	}
	public void setsFecha(String sFecha) {
		this.sFecha = sFecha;
	}
	public String getsHora() {
		return sHora;
	}
	public void setsHora(String sHora) {
		this.sHora = sHora;
	}
}