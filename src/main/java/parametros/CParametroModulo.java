package parametros;

public class CParametroModulo {
	private int iParametro;
	private int iModulo;
	private String sFecha;
	private String sHora;
	public int getiParametro() {
		return iParametro;
	}
	public void setiParametro(int iParametro) {
		this.iParametro = iParametro;
	}
	public int getiModulo() {
		return iModulo;
	}
	public void setiModulo(int iModulo) {
		this.iModulo = iModulo;
	}
	public String getsFecha() {
		return sFecha;
	}
	public void setsFecha(String sFecha) {
		this.sFecha = sFecha;
	}
	public String getsHora() {
		return sHora;
	}
	public void setsHora(String sHora) {
		this.sHora = sHora;
	}
}
