package financiero;

public class CRecargaTarifa {
	private long lRecarga;
	private int iTarifa;
	private String sFecha;
	private String sHora;
	
	public long getlRecarga() {
		return lRecarga;
	}
	public void setlRecarga(long lRecarga) {
		this.lRecarga = lRecarga;
	}
	public int getiTarifa() {
		return iTarifa;
	}
	public void setiTarifa(int iTarifa) {
		this.iTarifa = iTarifa;
	}
	public String getsFecha() {
		return sFecha;
	}
	public void setsFecha(String sFecha) {
		this.sFecha = sFecha;
	}
	public String getsHora() {
		return sHora;
	}
	public void setsHora(String sHora) {
		this.sHora = sHora;
	}
}
